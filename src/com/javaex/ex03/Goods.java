package com.javaex.ex03;

public class Goods {

	// 필드
	private String name;
	private int price;

	// 생성자

	// method -gettersetter
	public void setName(String n) {
		name = n;
	}

	public void setPrice(int p) {
		price = p;
	}

	public String getName() {
		return name;
	}
	
	public int getPrice () {
		return price;
		
		
	}
	

	// method 일반

}
